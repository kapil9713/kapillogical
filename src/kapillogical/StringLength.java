package kapillogical;

public class StringLength {
	
	public static void main(String[] args) {
		
		String str="Kapil Rathore";
		System.out.println(str.length()); //full length
		
		System.out.println(str.charAt(6));// character position
		
		System.out.println(str.indexOf('R'));
		
		System.out.println(str.indexOf('a', 6));
		
		
	}

}
